wprdp.setLayout = function(layout) {
	var browserType = _browser.name;
	if(browserType === 'safari') browserType = 'chrome';
	
	wprdp.current_sc = wprdp['sc_' + browserType + '_' + layout];

	switch (layout) {
	case "fr":
		wprdp.current_layout = 0x40C;
		break;
	case "uk":
		wprdp.current_layout = 0x809;
		break;
	case "es":
		wprdp.current_layout = 0x40A;
		break;
	case "de":
		wprdp.current_layout = 0x407;
		break;
	case "ru":
		wprdp.current_layout = 0x419;
		break;
	default:
		wprdp.current_layout = 0x409; // US
	}
	
	wprdp.current_layout_str = layout;
	
	if(typeof(Storage) !== "undefined") {
		localStorage.setItem("input_layout", layout);
	}
};

wprdp.getScanCode = function (keyEvent, keyId) {
	var numPadList = "1234567890.,/*-+";
	var numPadListBis = "";
	var scanCode = 0x0000;
	const KBD_FLAGS_EXTENDED = 0x0100;
	const KBD_FLAGS_STANDARD = 0x0000;
	var extensionFlag = KBD_FLAGS_STANDARD;
	var keyCode;
	if ( keyEvent.key ) {
		var curKey = keyEvent.key;
		
		if ( curKey === 'Unidentified' || keyId ) {
			curKey = keyId;
		}
		
		// Field present in Firefox and Internet Explorer
		// Managing num pad characters apart
		if ( numPadList.indexOf(curKey) == -1 ) // General case
		{
			if ( keyEvent.location == 3 ) { // Numpad key ( case of arrows, pageup, pagedown, etc... )
				curKey = 'numpad' + keyEvent.key;
			}
			scanCode = wprdp.current_sc[curKey];
		}
		else // NumPad case
		{
			if ( keyEvent.keyCode >= 96 && keyEvent.keyCode <= 111 ) {
				scanCode = wprdp.current_sc['numpad' + curKey]; // Numeric Pad
			}
			else {
				scanCode = wprdp.current_sc[curKey]; // Upper line figures
			}
		}
	}
	else {
		if ( keyId === undefined ) {
			keyCode = keyEvent.keyCode;
		} else {
			keyCode = keyId;
		}
		// Relying on keycode in Chrome
		if ( keyEvent.location == 3 ) { // Numpad key ( case of arrows, pageup, pagedown, etc... )
			keyCode = 'numpad' + keyCode;
		}
		//add protection for Safari
		if(wprdp.current_sc) {
			scanCode = wprdp.current_sc[keyCode];
		}
	}
		
	//fallback on firefox scanCode in the current layout
	if(!scanCode &&  wprdp["sc_firefox_" + wprdp.current_layout_str]) {
		scanCode = wprdp["sc_firefox_" + wprdp.current_layout_str][keyCode];
		
		//fallback on chrome
		if(!scanCode && wprdp["sc_chrome_" + wprdp.current_layout_str]) {
			scanCode = wprdp["sc_chrome_" + wprdp.current_layout_str][keyCode];
		}
	}
	
	return [ (scanCode & 0x100) ? KBD_FLAGS_EXTENDED : KBD_FLAGS_STANDARD, scanCode & 0xFF ];
};
