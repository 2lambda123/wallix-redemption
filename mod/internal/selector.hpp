    void draw(const Rect & clip)
    {
        this->gd.server_begin_update();
        this->gd.server_set_clip(clip);

        this->draw_background();
        this->draw_login();
        this->draw_array();
        this->draw_buttons();

        this->gd.server_end_update();
    }

